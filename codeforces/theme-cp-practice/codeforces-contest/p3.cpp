bool check(vector<bool>& c, vector<int>& a, int n) {
    vector<int> res(n);
    int l = 0;
    int r = 0;
    for (int i = n-1; i >= 1; i--) {
        if (!c[i]) r++;
    }
    for (int i = 0; i < n; i++) {
        res[i] = l + 1 + r;
        if (c[i]) l++;
        if (i+1 < n && !c[i+1]) r--;
    }
    for (int i = 0; i < n; i++) {
        if (res[i] != a[i]) return false;
    }
    return true;
}

